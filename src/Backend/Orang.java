package Backend;

/**
 *
 * @author Asus FX
 */
public abstract class Orang implements IButton{
    public String nama;
    public String alamat;
    public String telepon;

    public Orang() {
    }

    public Orang(String nama, String alamat,String telepon) {
        this.nama = nama;
        this.alamat = alamat;
        this.telepon = telepon;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
    
    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTelepon() {
        return telepon;
    }

    public void setTelepon(String telepon) {
        this.telepon = telepon;
    }
}
