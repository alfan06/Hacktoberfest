    public void tampilkanData(){
        String[] kolom = {"ID", "Jadwal"};
        ArrayList<JadwalBerangkat> list = new JadwalBerangkat().getAll();
        Object rowData[] = new Object[2];
        
        tblJadwal.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(JadwalBerangkat kat : list){
            rowData[0] = kat.getId_jadwal();
            rowData[1] = kat.getJadwalBerangkat();
            
            ((DefaultTableModel)tblJadwal.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] kolom = {"ID", "Jadwal"};
        ArrayList<JadwalBerangkat> list = new JadwalBerangkat().search(keyword);
        Object rowData[] = new Object[2];
        
        tblJadwal.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(JadwalBerangkat kat : list){
            rowData[0] = kat.getId_jadwal();
            rowData[1] = kat.getJadwalBerangkat();
            
            ((DefaultTableModel)tblJadwal.getModel()).addRow(rowData);
        }
    }

