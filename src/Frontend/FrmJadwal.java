    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        JadwalBerangkat kat = new JadwalBerangkat();
        kat.setId_jadwal(Integer.parseInt(txtIdjadwal.getText()));
        kat.setJadwalBerangkat(txtJadwal.getText());
        kat.save();
        txtIdjadwal.setText(Integer.toString(kat.getId_jadwal()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblJadwal.getModel();
        int row = tblJadwal.getSelectedRow();
        
        JadwalBerangkat kat = new JadwalBerangkat().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        kat.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

