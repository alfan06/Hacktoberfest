    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        MiniBus b = new MiniBus();
        b.setId_minibus(Integer.parseInt(txtIdBis.getText()));
        b.setKategoriminibus((KategoriMiniBus)cmbKategori.getSelectedItem());
        b.setMerk(txtMerek.getText());
        b.setKapasitas(Integer.parseInt(txtKapasitas.getText()));
        b.setTujuan((Tujuan)cmbTujuan.getSelectedItem());
        b.setJadwalberangkat((JadwalBerangkat)cmbJadwal.getSelectedItem());
        b.setHarga(Integer.parseInt(txtBiaya.getText()));
        b.save();
        txtIdBis.setText(Integer.toString(b.getId_minibus()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblMinibus.getModel();
        int row = tblMinibus.getSelectedRow();
        
        MiniBus b = new MiniBus().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        b.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

