    private void tblMinibusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMinibusMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblMinibus.getModel();
        int row = tblMinibus.getSelectedRow();
        MiniBus b = new MiniBus();
        
        b = b.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdBis.setText(String.valueOf(b.getId_minibus()));
        cmbKategori.getModel().setSelectedItem(b.getKategoriminibus());
        txtMerek.setText(b.getMerk());
        txtKapasitas.setText(String.valueOf(b.getKapasitas()));
        cmbTujuan.getModel().setSelectedItem(b.getTujuan());
        cmbJadwal.getModel().setSelectedItem(b.getJadwalberangkat());
        txtBiaya.setText(String.valueOf(b.getHarga()));
    }//GEN-LAST:event_tblMinibusMouseClicked

