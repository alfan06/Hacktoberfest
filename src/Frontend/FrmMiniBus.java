    public void tampilkanData(){
        String[] kolom = {"ID","Kategori","Merk","Kapasitas","Tujuan","Jadwal","Harga"};
        ArrayList<MiniBus> list = new MiniBus().getAll();
        Object rowData[] = new Object[7];
        
        tblMinibus.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(int i =0;i<list.size();i++){
            rowData[0] = list.get(i).getId_minibus();
            rowData[1] = list.get(i).getKategoriminibus().getNama();
            rowData[2] = list.get(i).getMerk();
            rowData[3] = list.get(i).getKapasitas();
            rowData[4] = list.get(i).getTujuan().getTujuan();
            rowData[5] = list.get(i).getJadwalberangkat().getJadwalBerangkat();
            rowData[6] = list.get(i).getHarga();
            
            ((DefaultTableModel)tblMinibus.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] kolom = {"ID","Kategori","Merk","Kapasitas","Tujuan","Jadwal","Harga"};
        ArrayList<MiniBus> list = new MiniBus().search(keyword);
        Object rowData[] = new Object[7];
        
        tblMinibus.setModel(new DefaultTableModel(new Object[][] {},kolom));
        
        for(MiniBus b : list){
            rowData[0] = b.getId_minibus();
            rowData[1] = b.getKategoriminibus().getNama();
            rowData[2] = b.getMerk();
            rowData[3] = b.getKapasitas();
            rowData[4] = b.getTujuan().getTujuan();
            rowData[5] = b.getJadwalberangkat().getJadwalBerangkat();
            rowData[6] = b.getHarga();
            
            ((DefaultTableModel) tblMinibus.getModel()).addRow(rowData);
        }
    }
    
