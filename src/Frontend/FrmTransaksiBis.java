    private void btnBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBeliActionPerformed
        // TODO add your handling code here:
        if((Integer.parseInt(txtTiket.getText())) <= (Integer.parseInt(txtKapasitas.getText()))){
            TransaksiBis b = new TransaksiBis();
            b.setId_transaksiBis(Integer.parseInt(txtIdTransaksi.getText()));
            b.setUser((User) cmbUser.getSelectedItem());
            b.setBis(new Bis().getById(Integer.parseInt(this.txtIdBis.getText())));
            b.setJmlhOrg(Integer.parseInt(txtTiket.getText()));
            b.setBiaya(Integer.parseInt(txtBiaya.getText()));
            b.setDate(this.txtDate.getText());
            b.save();
            txtIdTransaksi.setText(Integer.toString(b.getId_transaksiBis()));
            JOptionPane.showMessageDialog(null, "Pembelian sukses");
        }else{
            JOptionPane.showMessageDialog(null, "full sam");
        }
    }//GEN-LAST:event_btnBeliActionPerformed

