    public void tampilkanData(){
        String[] kolom = {"ID","Kategori","Merk","Kapasitas","Tujuan","Jadwal","Harga"};
        ArrayList<Bis> list = new Bis().getAll();
        Object rowData[] = new Object[7];
        
        tblBis.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(int i =0;i<list.size();i++){
            rowData[0] = list.get(i).getId_bis();
            rowData[1] = list.get(i).getKategoribis().getNama();
            rowData[2] = list.get(i).getMerk();
            rowData[3] = list.get(i).getKapasitas();
            rowData[4] = list.get(i).getTujuan().getTujuan();
            rowData[5] = list.get(i).getJadwalberangkat().getJadwalBerangkat();
            rowData[6] = list.get(i).getHarga();
            
            ((DefaultTableModel)tblBis.getModel()).addRow(rowData);
        }
    }
    
