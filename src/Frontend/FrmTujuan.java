    public void tampilkanData(){
        String[] kolom = {"ID", "Tujuan"};
        ArrayList<Tujuan> list = new Tujuan().getAll();
        Object rowData[] = new Object[2];
        
        tblTujuan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Tujuan kat : list){
            rowData[0] = kat.getId_tujuan();
            rowData[1] = kat.getTujuan();
            
            ((DefaultTableModel)tblTujuan.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] kolom = {"ID", "Tujuan"};
        ArrayList<Tujuan> list = new Tujuan().search(keyword);
        Object rowData[] = new Object[2];
        
        tblTujuan.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Tujuan kat : list){
            rowData[0] = kat.getId_tujuan();
            rowData[1] = kat.getTujuan();
            
            ((DefaultTableModel)tblTujuan.getModel()).addRow(rowData);
        }
    }

