    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Tujuan kat = new Tujuan();
        kat.setId_tujuan(Integer.parseInt(txtIdTujuan.getText()));
        kat.setTujuan(txtTujuan.getText());
        kat.save();
        txtIdTujuan.setText(Integer.toString(kat.getId_tujuan()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblTujuan.getModel();
        int row = tblTujuan.getSelectedRow();
        
        Tujuan kat = new Tujuan().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        kat.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

